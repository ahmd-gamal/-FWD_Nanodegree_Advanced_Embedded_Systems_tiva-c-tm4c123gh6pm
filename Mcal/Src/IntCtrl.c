/******************************************************************************
* \Syntax          : void IntCrtl_Init(void)                                      
* \Description     : initialize Nvic\SCB Module by parsing the Configuration 
*                    into Nvic\SCB registers                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/

void IntCrtl_Init(void)
{
   uint8 itr;
	 uint8 j,k;
	/*TODO Configure Grouping\SubGrouping System in APINT register in SCB*/
    SCB_REGS->APINT = VECTKEY_KEY|(GROUPPRIORITY_X_SUBPRIORITY_Y<<8);
    
  /*TODO : Assign Group\Subgroup priority in NVIC_PRIx Nvic and SCB_SYSPRIx Registers*/  

/* set group_priorty / subgroub priority in NVIC */
    for ( itr = 0; itr < NUM_OF_INT; itr++)
    {
        j = (Interrupt_Arr[itr][0]) / 4;
        k = (Interrupt_Arr[itr][0]) - 4 * j;

        NVIC_REGS->PRI[j] |= (Interrupt_Arr[itr][1] << (k * 8 + 5));
    }

	/*TODO : Enable\Disable based on user configurations in NVIC_ENx and SCB_Sys Registers */
	for ( itr = 0; itr < NUM_OF_INT; itr++)
    {
			  j = (Interrupt_Arr[itr][0]) / 32;
        k = (Interrupt_Arr[itr][0]) % 32;
        NVIC_REGS->EN[j] = INTERRUPT_GROUPING_STATUS << (k);
		}

}

